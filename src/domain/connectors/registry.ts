export type ConnectorId = (typeof CONNECTOR_REGISTRY)[number]["id"];

export function getConnector(id: string): ConnectorDefinition | undefined {
  return CONNECTOR_REGISTRY.find((connector) => connector.id === id);
}

const duplicateIds = CONNECTOR_REGISTRY.map((connector) => connector.id).filter(
  (id, index, ids) => ids.indexOf(id) !== index,
);
if (duplicateIds.length) throw new Error(`Duplicate connector ids: ${duplicateIds.join(", ")}`);
