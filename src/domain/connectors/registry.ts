import type { ConnectorDefinition } from "./types";

type Seed = Omit<
  ConnectorDefinition,
  "requiresRightsConfirmation" | "requiresOriginalSource" | "supportsPublicLinks"
> &
  Partial<
    Pick<
      ConnectorDefinition,
      "requiresRightsConfirmation" | "requiresOriginalSource" | "supportsPublicLinks"
    >
  >;

const define = (seed: Seed): ConnectorDefinition => ({
  requiresRightsConfirmation: true,
  requiresOriginalSource: false,
  supportsPublicLinks: false,
  ...seed,
});

